function pickOne(arr) {
  if (!Array.isArray(arr) || arr.length === 0) {
    return "無理しないでね。落ち着いたらまた話そ😊";
  }

  return arr[Math.floor(Math.random() * arr.length)];
}

function getReplyTemplates(scene = "default", plan = "free") {
  const safeScene = replyTemplates[scene] ? scene : "default";
  const safePlan = ["free", "premium", "pro"].includes(plan) ? plan : "free";

  return replyTemplates[safeScene][safePlan] || replyTemplates.default.free;
}

function getOneReply(scene = "default", plan = "free") {
  return pickOne(getReplyTemplates(scene, plan));
}

