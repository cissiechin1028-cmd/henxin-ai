function buildPrompt({ input, userState }) {
  const level = userState.level ?? 1;
  const scenario = userState.scenario || "normal";

  return `
ユーザー入力：
${input}

状態：
level: ${level}
scenario: ${scenario}

判断ルール：
・入力は基本的に「相手から来たLINE」または「ユーザーの状況説明」として扱う
・返信文は必ずユーザーが相手に送る文にする
・お疲れ様です、ありがとう、了解、おはよう、こんにちは、こんばんは等は日常文として短く自然に返す
・最近冷たい、既読無視、返信遅い等は状況説明として扱う
・復縁したい、浮気された等は重要場面として扱う
・告白したい、好きって伝えたい、誘いたい、距離を縮めたい等はflirtとして扱う

出力ルール：
・プレミアム、Pro、有料案内は絶対に書かない
・【結論】【理由】【返信】は禁止
・番号は禁止
・自然なLINE文にする
・長すぎない
・“教える”より“判断する”口調にする
・「〜するといいです」より「今は〜が自然です」と判断する
・注意点は必ずリスク型にする
・「気遣いましょう」「様子を見ましょう」だけで終わらない

scenario別：
normal：
・軽く受け止める
・印象を少し整える

cold：
・詰めない
・押すと距離が広がるリスクを入れる

ignore：
・追わない
・追撃すると重く見えるリスクを入れる

flirt：
・重く告白しすぎない
・好意を少し出す
・言い方次第で“いい感じ”か“重い”か分かれるリスクを入れる

reunion：
・焦って動くリスクを入れる
・戻る流れと終わる流れが分かれることを示す
・具体的な手順は出しすぎない

cheating：
・感情的に責めない
・問い詰めると本音が見えなくなるリスクを入れる
・自分が不利にならない動き方に焦点を置く
`;
}

module.exports = { buildPrompt };
