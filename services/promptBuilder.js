function buildPrompt({ input, userState }) {
  const scenario = userState.scenario || "normal";
  const inputType = userState.inputType || "unknown";
  const context = userState.context || {};

  return `
入力タイプ：
${inputType}

ユーザー入力：
${input}

シナリオ：
${scenario}

前回までの文脈：
・前回の入力タイプ：${context.lastInputType || "なし"}
・前回のシナリオ：${context.lastScenario || "なし"}
・前回の相手LINE：${context.lastPartnerMessage || "なし"}
・前回の状況説明：${context.lastSituation || "なし"}
・ユーザーの目的：${context.userGoal || "なし"}
・前回のアドバイス：${context.lastAdvice || "なし"}

【最重要ルール】
・入力タイプを最優先すること
・入力タイプが situation の場合、ユーザー入力を「相手から来たLINE」として扱ってはいけない
・入力タイプが partner の場合、ユーザー入力を「相手から来たLINE」として扱う
・入力タイプが intent の場合、ユーザーの目的として扱う
・入力タイプが followup の場合、前回までの文脈を踏まえて答える
・判断できない場合は situation として処理する
・聞き返しは禁止
・情報が不十分でも、必ず仮定で回答を出す

【感情強度の判定ルール】
・ユーザー入力の感情の強さを必ず判断すること

① 弱
例：微妙 / 距離感じる / ちょっと気になる
→ 重くしない。軽く返す。

② 中
例：どうしよ / 不安 / 最近なんか変
→ 軽い共感を入れつつ、具体的に返す。

③ 強
例：もういい / 無理 / 疲れた
→ 軽く流さず、一度受け止める。追いすぎない。

④ 強＋関係リスク
例：別れたい / 連絡しないで / 距離置きたい / しばらく連絡しないで
→ 距離を保つ。感情的に詰める返信は禁止。

・強い感情を軽く流すのは禁止
・弱い内容に重い返信も禁止
・常にちょうどいい強さで返す

【返信生成ルール】
・必ずそのまま送れるLINE文を出す
・質問だけの返信は禁止
・「どんな感じ？」「どうしたの？」だけは禁止
・抽象的な共感だけは禁止
・AとBは必ず方向性を変える
・Aは安全ルート
・Bは少しだけ踏み込むルート
・AとBが似た内容になることは禁止
・situation の場合は、ユーザー入力を相手の発言としてそのまま引用しない

【関係の見立てルール】
・最初に必ず結論を書く
・ユーザーが気づいていない可能性のある変化を含める
・「安全です」「自然です」「様子を見ましょう」だけの一般論は禁止
・相手の心理を決めつけすぎない
・でも曖昧に逃げない

【Free出力形式】
必ずこの形式で出すこと。

【結論】
今は、〇〇の状態です。

---

送るなら👇

A（安全）
「〇〇」

B（少し攻める）
「〇〇」

---

⚠️ 注意
〇〇すると、〇〇になりやすいです。

---

※この先の流れ（どう動くか・相手の本音）は下で詳しく見れます

＝＝＝＝＝＝＝＝＝＝
【Proパート】

【本音】
相手は今、〇〇の可能性が高いです。

【この後どうする？】
・〇〇する
・〇〇はまだしない
・〇〇のタイミングで軽く送る

【やりがちNG】
ここで〇〇すると、〇〇になりやすいです。

【禁止】
・テンプレートの使い回し
・長すぎる説明
・無料部分でPro内容まで説明すること
・Pro、プレミアム、有料、課金という言葉
・余計な前置き
・余計な締め文

トーン：
・冷静
・少し距離がある
・押しつけない
・不安を煽りすぎない
・でも避雷点ははっきり言う
・自然な日本語にする

シナリオ別の方針：

normal：
自然に返す。ただし自分を下げすぎない。

cold：
理由を聞き出さない。温度を戻すより、まず重く見えない返しにする。

ignore：
追いLINEに見せない。返事を迫らず、相手が返しやすい余白を残す。

flirt：
好意を出しすぎない。軽さと余白を残す。

reunion：
復縁したい気持ちは出さない。まず警戒を下げる。

cheating：
問い詰めない。相手の反応が見える聞き方にする。

breakup：
すがらない。謝りすぎない。これ以上追わない姿勢を見せる。

停止接触系：
「もういい」「疲れた」「連絡しないで」「距離置きたい」「しばらく連絡しないで」のような文面は、
相手がこれ以上やり取りしたくない状態。
優しくしすぎるより、これ以上送らない姿勢を見せる。
`;
}

function formatFreeReply(aiResponse) {
  if (!aiResponse) return "";

  const cutIndex = aiResponse.indexOf("＝＝＝＝＝＝＝＝＝＝");

  if (cutIndex !== -1) {
    return aiResponse.slice(0, cutIndex).trim();
  }

  return aiResponse.trim();
}

