function detectScenario(text = "") {
  const t = String(text).trim();

  // 浮気・怪しい
  if (
    /浮気|怪しい|隠して|女の子|男の子|他の人|嘘|泊まり|連絡取ってる|スマホ隠す|急に予定が増えた|誰かいる/.test(
      t
    )
  ) {
    return "cheating";
  }

  // 別れ・振られた
  if (
    /別れたい|別れよう|距離置きたい|距離を置きたい|もう無理|好きじゃない|冷めた|終わり|さよなら|連絡しないで|振られた|別れた/.test(
      t
    )
  ) {
    return "breakup";
  }

  // 復縁
  if (
    /復縁|元彼|元カノ|やり直し|戻りたい|また話したい|もう一度|よりを戻したい/.test(
      t
    )
  ) {
    return "reunion";
  }

  // 喧嘩・言い合い
  if (
    /喧嘩|けんか|言い合い|怒らせた|怒ってる|傷つけた|言いすぎた|謝りたい|仲直り/.test(
      t
    )
  ) {
    return "fight";
  }

  // 既読無視・未読無視
  if (
    /既読|未読|無視|返信ない|返事ない|返信こない|返ってこない|スルー|既読無視|未読無視/.test(
      t
    )
  ) {
    return "ignore";
  }

  // 温度低下・冷たい態度
  if (
    /冷たい|そっけない|距離|温度|返信遅い|忙しい|バタバタ|前より冷たい|態度が変わった|素っ気ない/.test(
      t
    )
  ) {
    return "cold";
  }

  // 片思い・曖昧な関係
  if (
    /好き|告白|誘いたい|デート|会いたい|気になる|脈あり|脈なし|片思い|アプローチ/.test(
      t
    )
  ) {
    return "flirt";
  }

  // 通常相談
  return "normal";
}

module.exports = { detectScenario };
