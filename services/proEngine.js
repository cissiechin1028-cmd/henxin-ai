// services/proEngine.js

function generateProResponse(input, scenario) {
  if (scenario === "reunion") {
    return `▼現状の見立て
今は「冷却〜再接触の境目」です
完全に切れてはいないが、距離はまだある状態です

▼リスク
ここで気持ちを強く出すと、
相手が負担に感じて距離を広げる可能性があります

▼最適な進め方（3ステップ）
① 軽く接触（近況レベル）
② 相手の温度を確認
③ 少しずつ距離を縮める

▼分岐
A：いきなり気持ちを伝える
→ 一時的に反応は来るが、長期的に離れやすい

B：段階的に近づく
→ 自然に関係が戻る可能性が高い

▼送るならこれ
「最近どうしてる？たまに話せたら嬉しいな」

▼タイミング
夜（20〜23時）

▼次の一手
・返信軽い → 2〜3往復様子見る
・返信なし → 3日空ける
・反応良 → 少し踏み込む`;
  }

  if (scenario === "cheating") {
    return `▼現状の見立て
疑い〜グレー状態です
相手はまだ完全に本音を出していない可能性があります

▼リスク
ここで問い詰めると、
相手が防御に入り本音が見えなくなります

▼最適な進め方
① 感情で責めない
② 少し距離を取る
③ 相手の出方を見る

▼分岐
A：感情で詰める
→ 隠れる可能性が高い

B：冷静に引く
→ 行動が見えやすくなる

▼送るならこれ
「最近ちょっと忙しそうだね」

▼次の一手
・言い訳多い → 黒寄り
・自然 → グレー
・連絡減る → 距離戦`;
  }

  if (scenario === "cold") {
    return `▼現状の見立て
温度が下がり始めている段階です

▼リスク
ここで詰めると一気に距離が広がります

▼最適な進め方
① 軽く接触
② 話題を広げすぎない
③ 相手の反応を見る

▼送るならこれ
「最近ちょっと忙しい？」

▼次の一手
・反応弱 → 引く
・普通 → 維持
・良 → 少し詰める`;
  }

  return `もう少し詳しく状況を教えてください`;
}

module.exports = { generateProResponse };
