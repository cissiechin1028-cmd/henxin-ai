// services/proEngine.js

function makeHook(input = "") {
  const text = String(input).trim();

  if (!text) return "この状況";

  return text.length > 24
    ? `「${text.slice(0, 24)}…」`
    : `「${text}」`;
}

function generateProResponse(input, scenario) {
  const hook = makeHook(input);

  if (scenario === "breakup") {
    return `▼現状の見立て
${hook} という流れなら、
今は「説得するほど離れやすい段階」です。

相手は感情的に疲れている可能性があるので、
ここで長文や謝罪を重ねると、さらに距離を置かれやすくなります。

▼リスク
すがる言い方をすると、
相手の中で「もう終わらせたい」という気持ちが強まりやすいです。

▼今の最適な動き方
まずは相手の負担を増やさず、
少しだけ余白を残す返し方が安全です。

▼送るならこれ
「気持ちはわかったよ。すぐに返事を求めたりしないから、少し落ち着いてから話せたら嬉しい」

▼次の一手
返信が来たら、すぐに詰めずに短く返す。
返信がなければ、数日は追わない方が安全です。`;
  }

  if (scenario === "reunion") {
    return `▼現状の見立て
${hook} という流れなら、
今は「戻したい気持ち」よりも「警戒を下げること」が大事な段階です。

▼リスク
ここで気持ちを強く出すと、
相手が重く感じて距離を取りやすくなります。

▼今の最適な動き方
軽く接触して、相手の温度を見ます。
最初から復縁の話に入らない方が安全です。

▼送るならこれ
「久しぶり。最近どうしてる？無理に返さなくて大丈夫だけど、少し話せたら嬉しいな」

▼次の一手
反応が軽いなら、2〜3往復は雑談で止める。
反応が良ければ、少しずつ距離を戻します。`;
  }

  if (scenario === "cheating") {
    return `▼現状の見立て
${hook} という状況なら、
まだ確定ではなく「違和感がある段階」です。

▼リスク
ここで問い詰めると、
相手が防御に入り、本音や行動が見えにくくなります。

▼今の最適な動き方
責めずに、相手の返し方を見る聞き方が安全です。

▼送るならこれ
「最近ちょっと様子違う気がして。何かあった？」

▼次の一手
返答が自然なら様子見。
ごまかしが多いなら、次は少し距離を置いて行動を見る方が安全です。`;
  }

  if (scenario === "ignore") {
    return `▼現状の見立て
${hook} という流れなら、
今は追うほど相手の温度が下がりやすい段階です。

▼リスク
連続で送ると、
相手に「返さなきゃ」という負担だけが残りやすいです。

▼送るならこれ
「忙しかったら全然大丈夫。また落ち着いたら話そ😊」

▼次の一手
この後は一度止める。
返信が来たら、責めずに普通の温度で返します。`;
  }

  if (scenario === "cold") {
    return `▼現状の見立て
${hook} という流れなら、
相手の温度が少し下がっている可能性があります。

▼リスク
ここで理由を聞きすぎると、
さらに距離を取られることがあります。

▼送るならこれ
「最近ちょっと忙しそうだね。無理しないでね」

▼次の一手
反応が薄ければ一度引く。
普通に返ってきたら、軽い話題で続けるのが安全です。`;
  }

  return `▼現状の見立て
${hook} なら、
今は自然に返して相手の反応を見る場面です。

▼送るならこれ
「そうなんだね。無理しないでね😊」

▼次の一手
相手の返信が軽ければ、こちらも軽く返すのが安全です。`;
}

module.exports = { generateProResponse };
