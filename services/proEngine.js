・まずは警戒を下げる
・復縁したい気持ちはまだ出さない
・2〜3往復は軽い雑談で温度を見る

【送るなら】
「久しぶり。最近どうしてる？無理に返さなくて大丈夫だけど、少し話せたら嬉しいな」

【言い換え】
「久しぶり。元気にしてる？急にごめんね、少しだけ話せたら嬉しい」
「最近ふと思い出して連絡したよ。無理に返さなくて大丈夫」

【タイミング】
・夜よりも重く見えにくい夕方〜早めの夜
・相手の返信が軽ければ、復縁話にはまだ入らない
