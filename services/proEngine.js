// services/proEngine.js

function generateProResponse(input, scenario) {

  // 👇 从用户输入提取一点“情绪钩子”
  const hook = input.length > 20
    ? `「${input.slice(0, 20)}…」`
    : `「${input}」`;

  if (scenario === "reunion") {
    return `▼現状の見立て
${hook} って感じだと、
今は「冷却〜再接触の境目」にいる可能性が高いです

まだ完全に切れてはいないけど、
動き方で戻るか、そのまま終わるかが分かれる位置です

▼リスク
ここで気持ちを強く出すと、
相手が「まだその気じゃない」と感じて距離を取ることがあります

▼最適な進め方（3ステップ）
① 軽く接触（近況レベル）
② 相手の温度を見る
③ タイミングを見て少し踏み込む

▼分岐
A：今すぐ気持ちを伝える  
→ 一時的に反応は来るが、その後距離が固定されやすい

B：段階的に近づく  
→ 警戒が下がり、自然に戻る可能性が高い

▼送るならこれ
「最近どうしてる？たまに話せたら嬉しいな」

▼タイミング
夜（20〜23時）

▼次の一手
・返信軽い → 2〜3往復様子見る  
・返信なし → 3〜5日空ける  
・反応良 → 次回で少し踏み込む`;
  }

  if (scenario === "cheating") {
    return `▼現状の見立て
${hook} という状況だと、
まだ確定ではないけど「グレーゾーン」にいる可能性が高いです

▼リスク
ここで問い詰めると、
相手が防御に入り、本音が見えなくなるパターンが多いです

▼最適な進め方
① 感情で責めない  
② 一度距離を取る  
③ 相手の行動を観察する

▼分岐
A：感情で詰める  
→ 一時的に謝るが、隠れる可能性が高い

B：冷静に引く  
→ 行動が見えやすくなり、判断しやすくなる

▼送るならこれ
「最近ちょっと忙しそうだね」

▼次の一手
・言い訳が多い → 黒寄り  
・自然な返答 → グレー  
・連絡減る → 距離戦に移行`;
  }

  if (scenario === "cold") {
    return `▼現状の見立て
${hook} という流れだと、
温度が少しずつ下がっている段階です

▼リスク
ここで詰めると、
一気に距離が広がる可能性があります

▼最適な進め方
① 軽く接触  
② 話題を広げすぎない  
③ 相手の反応を見る

▼送るならこれ
「最近ちょっと忙しい？」

▼次の一手
・反応弱 → 引く  
・普通 → 維持  
・良 → 少し詰める`;
  }

  return `もう少し詳しく状況を教えてください`;
}

module.exports = { generateProResponse };
